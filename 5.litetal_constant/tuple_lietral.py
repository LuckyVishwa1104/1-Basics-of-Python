# Tuple literal - (val1 , val2 , val3 , - - - , valn)
''' Tuple - variable(A to Z, a to z, 0 to 9, _ )
   value = variables, input, output,     
                 expression, all data-types '''
# Example of Tuple collection literal.
# Example 1.
tuple_1=("carbon","nitrogen","oxygen","fluorien")
print(tuple_1)
print()
# Example 2.
tuple_2=("43",47,43,50,48)
print(tuple_2)
print()
# Example 3.
tuple_3=('@','#','$','&',"€","¶","∆")
print(tuple_3)
print()
# Example 4.
a,b=77.8,0b100001
tuple_4=({5,77},"lucky","the_one",a,78,8+52j,"π","®",b)
print(tuple_4)
# Example 5
tuple_5=(1,2.2,3+7j,0b111,(0o123,0x23A2E),["~","€","¥","π"])
print(tuple_5)